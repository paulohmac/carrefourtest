import Foundation
import UIKit

extension UILabel {
    func setDefaultFont(){
        self.font = UIFont(name:"Mona-San", size: 14.0)
    }
}

extension UITextField {
    func setDefaultFont(){
        self.font = UIFont(name:"Mona-San", size:  14.0)
    }
}
